weather = "stormy"

if weather == "sunny":
    print("It's very hot today.")
elif weather == "cloudy":
    print("The sky is very gloomy today.")
elif weather == "windy":
    print("The wind is blowing a lot today.")
elif weather == "rainy":
    print("It's raining a lot today.")
elif weather == "stormy":
    print("The rain is heavy and the winds are strong today.")
else:
    print("Invalid weather")